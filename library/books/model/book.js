const mongoose = require("mongoose");

//model
mongoose.model("Book", {
  // title,author,numberPages,publisher
  title: {
    type: String,
    required: true,
  },
  author: {
    type: String,
    required: true,
  },
  numberOfPages: {
    type: Number,
    required: false,
  },
  publisher: {
    type: String,
    required: false,
  },
});
